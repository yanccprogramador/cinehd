    public Shares(Usuario user){
         sessionFactory = HibernateUtil.getSessionFactory();
        s = sessionFactory.getCurrentSession();
        s.beginTransaction();
        initComponents();
        this.user = user;
            Query q= s.getNamedQuery("Compartilhamentos.findAll");
            List<br.com.yanchristoffer.entity.Compartilhamentos> shares =  q.list();
            DefaultTableModel model =(DefaultTableModel) jTable1.getModel();
            for (br.com.yanchristoffer.entity.Compartilhamentos share : shares){
                model.addRow(new Object[] {share.getHd(),share.getFilme()});
            }
        s.clear();    
    }
